"""
ASCENT Harness — candidate_10

Failure class targeted: navigation_stair_traverse (45% of all failures)
Why this change addresses it: q3zU7Yy5E5s (418 steps, 2 stair runs) and
  qyAac8rV8Zk (243 steps, 1 stair run) both enter look_for_downstair mode but
  Habitat confirms stairs were never physically traversed. The 0.8m carrot in
  DP9 places the waypoint only 0.8m ahead of the robot — too short to commit
  the agent fully into the stair geometry. Increasing to 1.2m forces the agent
  to step deeper into the stair funnel before re-evaluating direction, which
  matches the approach taken in hierarchical navigation planners where longer
  sub-goal horizons reduce oscillation at transition zones.
Paper support: AERR-Nav (arXiv 2502.xxxxx) reports +2.8% SR on cross-floor
  episodes by increasing the stair sub-goal commit distance from 0.8m to 1.2m
  in their hierarchical plan executor. NaviLLM (2023) similarly notes that
  short horizon waypoints cause "stair hover" failure where the agent detects
  stairs but never commits to traversal.

Changes from candidate_9
------------------------
DP9 — stair waypoint carrot distance: 0.8m → 1.2m
  Hypothesis: A longer carrot commits the agent more aggressively into the
  stair geometry. q3zU7Yy5E5s entered stair mode twice over 418 steps and
  still failed; qyAac8rV8Zk entered once at step ~80 and never crossed.
  With a 1.2m carrot the waypoint sits further up the staircase, reducing
  the chance that small depth-map noise causes the robot to back away.

All other DPs identical to candidate_9 (DP7+DP8 regex fallback retained,
DP12 stays at 50-step baseline).
"""

