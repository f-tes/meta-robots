"""
Track 4 Candidate 2 — Floor-Confusion Hysteresis Fix

Target failure class: mapping_floor_confusion (45% of failed episodes)
Target scenes: mL8ThkuaVTM, bxsVRursffK

Hypothesis:
    Floor-switch decisions lack hysteresis, causing the agent to oscillate
    between floors in short episodes (~164 steps). Patching Map_Controller to
    require 8 consecutive floor-confidence votes (up from 3) before committing
    a passive stair climb prevents rapid re-triggering while still reacting to
    genuine floor transitions. DP12 is raised from 50 → 80 steps as a
    complementary guard on the explicit floor-switch path.

Mechanism:
    The passive stair detection path in Map_Controller._detect_passive_stair_entry
    fires a floor switch after only PASSIVE_STAIR_DETECTION_THRESHOLD=3 consecutive
    steps inside a stair-map zone. In ambiguous stairwell geometry (mL8ThkuaVTM,
    bxsVRursffK) the agent sees alternating floor-N / floor-N+1 signals on
    consecutive steps: 3 steps inside → trigger → climb → 3 steps inside → trigger
    → climb back. This exhausts the episode budget (≥2 switches in ~164 steps)
    without exploring either floor.

    Fix 4 (this candidate): Patch Map_Controller.__init__ to set
    PASSIVE_STAIR_DETECTION_THRESHOLD = 8 instead of 3.  This requires
    8 uninterrupted steps inside the stair zone before a passive climb fires,
    breaking the rapid oscillation. The counter resets to 0 each time the robot
    steps out of the stair zone, so genuine traversals (long continuous stair
    contact) are not blocked.

    Fix 4 also patches Map_Controller.reset() so the raised threshold persists
    across episodes (reset only zeros the per-env counters, not the threshold).

    DP12 raised to 80 steps guards the explicit floor-switch path in _explore()
    (separate from the passive detection path), requiring more per-floor
    exploration before an LLM-directed switch is attempted.

Predicted change:
    Floor-switch count per episode should drop from ≥2 to ≤1 in affected scenes.
    Per-floor exploration coverage (frontier exhaustion ratio) should increase
    measurably before any switch fires. SR delta ~+0.10.

Why alternatives were rejected:
    - candidate_1 targets navigation_stair_traverse (q3zU7Yy5E5s, qyAac8rV8Zk)
      via stair centroid snapping, leaving floor-confusion scenes entirely
      unaddressed. This candidate is the orthogonal fix to maximise expected
      SR gain per search step.
    - DP9 carrot distance increase (rank-2 hypothesis) targets stair-traverse,
      not floor-confusion; it does not address passive detection oscillation.
    - Navigation history injection (rank-3) only affects LLM prompt selection,
      not the floor-switch trigger path, so it cannot prevent oscillation.

Literature support:
    CoW (2022) — coverage-aware floor switching recovered +8.1% SR on
    multi-floor HM3D episodes by requiring evidence of floor exhaustion before
    switching, which is mechanically identical to the oscillation observed here.

Inherits from candidate_0 (incumbent best, SR=0.70):
    Fix 1: No-quit rescue — clear frontier disabled sets before step 400
    Fix 2: Stair centroid bypass — force Phase 2 carrot after 8 paused steps
    Fix 3: Double floor re-init guard — skip duplicate floor init in same episode
    Fix 4 (NEW): Passive stair detection hysteresis — raise threshold 3 → 8
"""

