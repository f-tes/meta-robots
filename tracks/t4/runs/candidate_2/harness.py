    # ── Structural SDPs ──────────────────────────────────────────────────────

    def apply(self) -> None:
        """
        SDP-A: Startup monkey-patches.

        Fix 1 (no-quit): rescue early frontier exhaustion before step 400.
        Fix 2 (stair centroid bypass): skip Phase 1 after 8 paused steps.
        Fix 3 (double floor re-init guard): skip duplicate floor init.
        Fix 4 (NEW, floor-confusion hysteresis): raise passive stair
            detection threshold 3 → 8 steps via Map_Controller.__init__ patch.
        """
        import numpy as np
        import ascent.ascent_policy as _ap_mod
        import ascent.map_controller as _mc_mod

        # ── Tunable thresholds ───────────────────────────────────────────────
        _NOQUIT_MIN_STEPS = 400
        _MAX_RESCUES = 2
        _CENTROID_BYPASS_STEPS = 8
        _PASSIVE_HYSTERESIS_THRESHOLD = 8   # Fix 4: was 3

        # Shared per-env episode state (reset when num_steps[env] == 0).
        _ep_state = {}   # env → {"rescues": int, "floor_init_done": set()}

        def _reset_ep_state(env):
            _ep_state[env] = {"rescues": 0, "floor_init_done": set()}

        # ── Fix 1: No-quit rescue ────────────────────────────────────────────
        _orig_explore = _ap_mod.Ascent_Policy._explore

        def _patched_explore(policy_self, observations, env, masks):
            if policy_self._num_steps[env] == 0 or env not in _ep_state:
                _reset_ep_state(env)

            result = _orig_explore(policy_self, observations, env, masks)

            steps_used = policy_self._num_steps[env]
            st = _ep_state[env]
            if (result.item() != 0
                    or steps_used >= _NOQUIT_MIN_STEPS
                    or st["rescues"] >= _MAX_RESCUES):
                return result

            st["rescues"] += 1
            print(
                f"[T4_NOQUIT] env={env} step={steps_used} — early frontier exhaustion, "
                f"rescue {st['rescues']}/{_MAX_RESCUES} "
                f"({_NOQUIT_MIN_STEPS - steps_used} steps remaining budget)"
            )
            om = policy_self._map_controller._obstacle_map[env]
            om._disabled_frontiers.clear()
            om._disabled_frontiers_px = np.array([], dtype=np.float64).reshape(0, 2)
            om._this_floor_explored = False
            om._reinitialize_flag = False
            om._explored_up_stair = False
            om._explored_down_stair = False
            return policy_self._handle_stairwell_reinitialization(env, masks)

        _ap_mod.Ascent_Policy._explore = _patched_explore

        # ── Fix 2: Stair centroid bypass ─────────────────────────────────────
        _orig_climb_stair = _ap_mod.Ascent_Policy._climb_stair

        def _patched_climb_stair(policy_self, observations, env, ori_masks):
            mc = policy_self._map_controller
            paused = mc._obstacle_map[env]._climb_stair_paused_step
            centroid_reached = mc._reach_stair_centroid[env]

            if not centroid_reached and paused >= _CENTROID_BYPASS_STEPS:
                print(
                    f"[T4_CENTROID_BYPASS] env={env} paused={paused} steps — "
                    f"centroid unreachable, forcing Phase 2 (carrot strategy)"
                )
                mc._reach_stair_centroid[env] = True

            return _orig_climb_stair(policy_self, observations, env, ori_masks)

        _ap_mod.Ascent_Policy._climb_stair = _patched_climb_stair

        # ── Fix 3: Double floor re-init guard ────────────────────────────────
        _orig_new_floor_init = _mc_mod.Map_Controller._handle_new_floor_initialization

        def _patched_new_floor_init(mc_self, env, climb_direction):  # noqa: E306
            if env not in _ep_state:
                _reset_ep_state(env)

            target_floor = mc_self._cur_floor_index[env] + (
                1 if climb_direction == 1 else -1
            )
            done_set = _ep_state[env]["floor_init_done"]

            if target_floor in done_set:
                print(
                    f"[T4_INIT_GUARD] env={env} — skipping duplicate init for "
                    f"floor {target_floor}, advancing floor index directly"
                )
                if climb_direction == 1:
                    mc_self._obstacle_map[env]._explored_up_stair = True
                    mc_self._cur_floor_index[env] += 1
                else:
                    mc_self._obstacle_map[env]._explored_down_stair = True
                    mc_self._cur_floor_index[env] -= 1
                mc_self._update_current_maps(env)
                return

            done_set.add(target_floor)
            _orig_new_floor_init(mc_self, env, climb_direction)

        _mc_mod.Map_Controller._handle_new_floor_initialization = _patched_new_floor_init

        # ── Fix 4: Passive stair detection hysteresis ─────────────────────────
        # Raise PASSIVE_STAIR_DETECTION_THRESHOLD from 3 → 8 so the agent must
        # remain continuously inside a stair zone for 8 steps before a passive
        # floor switch fires.  This breaks the oscillation in ambiguous stairwell
        # geometry where the agent alternates in/out of the stair map every few
        # steps.  The counter resets on any step outside the zone, so genuine
        # traversals (long uninterrupted stair contact) are not blocked.
        _orig_mc_init = _mc_mod.Map_Controller.__init__

        def _patched_mc_init(mc_self, *args, **kwargs):
            _orig_mc_init(mc_self, *args, **kwargs)
            mc_self.PASSIVE_STAIR_DETECTION_THRESHOLD = _PASSIVE_HYSTERESIS_THRESHOLD
            print(
                f"[T4_HYSTERESIS] Map_Controller created — "
                f"PASSIVE_STAIR_DETECTION_THRESHOLD set to {_PASSIVE_HYSTERESIS_THRESHOLD}"
            )

        _mc_mod.Map_Controller.__init__ = _patched_mc_init

        # Also patch reset() so the threshold survives per-episode resets
        # (reset() only zeros the step counters, not the class-level threshold,
        # but guard in case a future version starts resetting it).
        _orig_mc_reset = _mc_mod.Map_Controller.reset

        def _patched_mc_reset(mc_self, env):
            _orig_mc_reset(mc_self, env)
            if mc_self.PASSIVE_STAIR_DETECTION_THRESHOLD != _PASSIVE_HYSTERESIS_THRESHOLD:
                mc_self.PASSIVE_STAIR_DETECTION_THRESHOLD = _PASSIVE_HYSTERESIS_THRESHOLD
                print(
                    f"[T4_HYSTERESIS] env={env} reset — restoring "
                    f"PASSIVE_STAIR_DETECTION_THRESHOLD to {_PASSIVE_HYSTERESIS_THRESHOLD}"
                )

        _mc_mod.Map_Controller.reset = _patched_mc_reset

